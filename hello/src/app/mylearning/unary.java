package app.mylearning;

public class unary {
	public static void main(String[] args) {
int a=10;
int b=111;
System.out.println(a++);//10
System.out.println(a--);//11
System.out.println(++a);//11
System.out.println(--a);//10
System.out.println(b++);//111
System.out.println(b--);//112
System.out.println(++b);//112
System.out.println(--b);//111
}
}
